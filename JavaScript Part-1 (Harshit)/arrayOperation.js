// array push pop
// array shift unshift
//mutable means change array add something

let fruit = ["apple", "banana", "mango"];
console.log(fruit);

//push : add at last
fruit.push("grapes");
console.log(fruit);

//pop
fruit.pop();
console.log(fruit);

//Unshift : add starting
fruit.unshift("banana");
console.log(fruit);

//shift : starting se remove
fruit.shift("apple");
console.log(fruit);